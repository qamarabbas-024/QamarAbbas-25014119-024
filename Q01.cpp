/* Q1. Write a program to assigned values to three variables at the time of declaration.
 Print the assigned values on the computer screen*/
#include<iostream>
using namespace std ;
int main()
{
    int A = 10;
    int B = 20;
    int C = 30;
    cout << "Value of A = " << A << endl;
    cout << "Value of B = " << B << endl;
    cout << "Value of C = " << C ;
}